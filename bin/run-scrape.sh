#!/bin/bash
# Run the Apify scrape pipeline: IG hashtag scraper + YouTube scraper in parallel.
# Outputs ~/content-scraper/{ig,yt,x}.json then runs process.py to build the ranked CSV.
#
# Requires: APIFY_TOKEN env var, curl, python3.
# Cost: ~$0.50 per run (IG + YT only; X skipped — free tier returns demo data).
# Wall time: 3-10 min depending on result count and Apify queue depth.

set -euo pipefail

TOKEN="${APIFY_TOKEN:?APIFY_TOKEN environment variable is required}"
WORK_DIR="${HOME}/content-scraper"
mkdir -p "$WORK_DIR"

# Resolve repo root from the script's location
SCRIPT_DIR="$(cd "$(dirname "$0")" && pwd)"
REPO_DIR="$(cd "$SCRIPT_DIR/.." && pwd)"

echo "→ Apify scrape · IG + YT in parallel"
echo "  work dir: $WORK_DIR"

# --- Kick off IG hashtag scraper ---
IG_RESP=$(curl -s -X POST \
  "https://api.apify.com/v2/acts/apify~instagram-hashtag-scraper/runs?token=$TOKEN" \
  -H "Content-Type: application/json" \
  -d '{"hashtags":["claudecode","aiagents","aiautomation","n8n","vibecoding"],"resultsType":"reels","resultsLimit":25}')
IG_RUN=$(echo "$IG_RESP" | python3 -c "import json,sys; print(json.load(sys.stdin)['data']['id'])")
IG_DATASET=$(echo "$IG_RESP" | python3 -c "import json,sys; print(json.load(sys.stdin)['data']['defaultDatasetId'])")
echo "  IG run started: $IG_RUN"

# --- Kick off YouTube scraper ---
YT_RESP=$(curl -s -X POST \
  "https://api.apify.com/v2/acts/streamers~youtube-scraper/runs?token=$TOKEN" \
  -H "Content-Type: application/json" \
  -d '{"searchQueries":["Claude Code","AI agents","vibe coding","N8N automation"],"maxResultsShorts":20,"maxResults":0,"maxResultStreams":0,"dateFilter":"week","downloadSubtitles":true,"preferAutoGeneratedSubtitles":true,"subtitlesLanguage":"en","subtitlesFormat":"plaintext","sortVideosBy":"POPULAR"}')
YT_RUN=$(echo "$YT_RESP" | python3 -c "import json,sys; print(json.load(sys.stdin)['data']['id'])")
YT_DATASET=$(echo "$YT_RESP" | python3 -c "import json,sys; print(json.load(sys.stdin)['data']['defaultDatasetId'])")
echo "  YT run started: $YT_RUN"

# --- Poll both until done ---
echo "→ Polling (every 30s)..."
while true; do
  IG_STATUS=$(curl -s "https://api.apify.com/v2/actor-runs/$IG_RUN?token=$TOKEN" \
    | python3 -c "import json,sys; print(json.load(sys.stdin)['data']['status'])")
  YT_STATUS=$(curl -s "https://api.apify.com/v2/actor-runs/$YT_RUN?token=$TOKEN" \
    | python3 -c "import json,sys; print(json.load(sys.stdin)['data']['status'])")
  echo "  $(date +%H:%M:%S)  IG=$IG_STATUS  YT=$YT_STATUS"
  if [[ "$IG_STATUS" != "RUNNING" && "$IG_STATUS" != "READY" \
     && "$YT_STATUS" != "RUNNING" && "$YT_STATUS" != "READY" ]]; then
    break
  fi
  sleep 30
done

if [[ "$IG_STATUS" != "SUCCEEDED" ]]; then
  echo "⚠️  IG status: $IG_STATUS (expected SUCCEEDED)" >&2
fi
if [[ "$YT_STATUS" != "SUCCEEDED" ]]; then
  echo "⚠️  YT status: $YT_STATUS (expected SUCCEEDED)" >&2
fi

# --- Fetch datasets ---
echo "→ Fetching datasets..."
curl -s "https://api.apify.com/v2/datasets/$IG_DATASET/items?token=$TOKEN&format=json" > "$WORK_DIR/ig.json"
curl -s "https://api.apify.com/v2/datasets/$YT_DATASET/items?token=$TOKEN&format=json" > "$WORK_DIR/yt.json"
echo "[]" > "$WORK_DIR/x.json"

IG_COUNT=$(python3 -c "import json; print(len(json.load(open('$WORK_DIR/ig.json'))))")
YT_COUNT=$(python3 -c "import json; print(len(json.load(open('$WORK_DIR/yt.json'))))")
echo "  ig: $IG_COUNT items · yt: $YT_COUNT items"

# --- Process via process.py ---
echo "→ Running process.py..."
cp "$REPO_DIR/content-scraper/process.py" "$WORK_DIR/process.py"
cd "$WORK_DIR"
python3 process.py 2>&1 | grep -E "raw counts|filter:|VIRAL|Output" | head -5

echo "✅ Scrape done · $WORK_DIR/"
ls -la "$WORK_DIR/"*_ai-niche-trends.* 2>/dev/null | tail -2 || true
